public class Main {
    public static void main(String[] args) {
        int i = 100;
        float f = 20.5f;
        double d = 40.6;
        boolean b = true;
        char c = 'C';
        String str = "Vietnam";
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("str = " + str);
    }
}
